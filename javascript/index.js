function playSound1() {
    var sound = document.getElementById("audio1");
    var hi = document.getElementById("draggable1");
    var rect = hi.getBoundingClientRect();
    // console.log("top" + rect.top + "right" + rect.right + "bottom" + rect.bottom + "left" + rect.left);
    if (rect.top > 200) {
        sound.play();
    }
    else {
        sound.pause();
    }

}

